SECTION "Main", ROM0
Main:
	; enable all interrupts 
	ld hl, $ffff
	ld [hl], $ff
	
	;clear wram
	ld hl, OAM_BUF
	ld bc, OAM_BUF_END - OAM_BUF
	xor a, a; ld a, 0
.clear_wram_loop:
	ld [hli], a
	dec c; only works because OAM_BUF_END - OAM_BUF <= 256
	jr nz, .clear_wram_loop

	; bg uses $8800 + $9000
	ld a, %10000011
	ld [LCD_CTRL], a

	; setup palettes
    ld a, %10000000
    ld [BCPS], a

    ; setup palette data
    ld hl, PALETTE_BG0
    REPT 8
    ldi a, [hl]
    ld [BCPD], a
	ENDR
    
    ld bc, %0000000000000000  ; transparent
    ld a, c
    ld [$ff6b], a
    ld a, b
    ld [$ff6b], a
    ld bc, %0010110100100101  ; dark
    ld a, c
    ld [$ff6b], a
    ld a, b
    ld [$ff6b], a
    ld bc, %0100000111001101  ; med
    ld a, c
    ld [$ff6b], a
    ld a, b
    ld [$ff6b], a
    ld bc, %0100001000010001  ; white
    ld a, c
    ld [$ff6b], a
    ld a, b
    ld [$ff6b], a

    ; set sprite0 palette
	ld a, %11100100
	;ld a, %11001111
	ld [OBJ0_PAL], a

	call load_dma_routine

    reset
    call read_joypad
    call wait_vblank
    call lcd_off

    call load_game_data

    call lcd_on

.Main_loop:
	ei
	halt
	ld a, [VBLANK_FLAG]
	and a;check for a == 0
	jr z, .Main_loop
	xor a, a
	ld [VBLANK_FLAG], a
	call DMA_ROUTINE

    call draw_game
    call read_joypad
    call update_game
	jr .Main_loop


TilesEnd:

